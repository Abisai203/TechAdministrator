class Refaccion {
    constructor(id, nombreDeLaRefaccion, numeroDeParte, marca, precio, condicion) {
        this.id = id;
        this.nombreDeLaRefaccion = nombreDeLaRefaccion; // Nombre de la refacción
        this.numeroDeParte = numeroDeParte; // Número de parte
        this.marca = marca; // Marca
        this.precio = precio; // Precio
        this.condicion = condicion; // Condición
    }
}

export default Refaccion;
